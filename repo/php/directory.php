///////////////////////////////////////////////////////////////////////////////
// Invoke the opentheory program.
///////////////////////////////////////////////////////////////////////////////

function opentheory($action,$args) {
  $cmd =
OPENTHEORY_BIN .
' -d ' . DIRECTORY_PATH . ' ' .
$action . $args .
' 2>&1 >> ' . LOG_PATH;

  return shell_exec($cmd);
}

